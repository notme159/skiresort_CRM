    private void chTypActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chTypActionPerformed
        if (chTyp.getSelectedItem() == "Časová") {
            cbCasova.setEnabled(true);
            cbBodova.setEnabled(false);
            permInfo[2] = (String) chTyp.getSelectedItem();
        } else if (chTyp.getSelectedItem() == "Bodová") {
            cbCasova.setEnabled(false);
            cbBodova.setEnabled(true);
            permInfo[2] = (String) chTyp.getSelectedItem();
        }
        if (permInfo[0] != null && permInfo[1] != null && permInfo[2] != null) {
            btOk.setEnabled(true);
        } else {
            btOk.setEnabled(false);
        }

    }//GEN-LAST:event_chTypActionPerformed

    private void cbCasovaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbCasovaActionPerformed
        if (cbCasova.getSelectedItem() == "2 hodiny") {
            cena = 270;
        } else if (cbCasova.getSelectedItem() == "4 hodiny") {
            cena = 370;
        } else if (cbCasova.getSelectedItem() == "celý den") {
            cena = 500;
        }
        lblCena.setText("Cena: " + cena);
        permInfo[1] = String.valueOf(cena);
        if (permInfo[0] != null && permInfo[1] != null && permInfo[2] != null) {
            btOk.setEnabled(true);
        } else {
            btOk.setEnabled(false);
        }
    }//GEN-LAST:event_cbCasovaActionPerformed

