    private String[] ziskejInfoZJListZak() {
        if (lstZak.getSelectedValue() != null) {
            String stringZakId = (String) lstZak.getSelectedValue();
            String[] rozdeleni = stringZakId.split(" ");
            permInfo[0] = rozdeleni[0];
            return permInfo;
        }
        return null;
    }
