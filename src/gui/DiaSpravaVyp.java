    private void btPridProdDoListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPridProdDoListActionPerformed

        int row = jTaProd.getSelectedRow();

        Produkt p = null;

        if (row != -1) {

            prodId = (int) jTaProd.getModel().getValueAt(row, 0);

            for (AidProdInfo aidProdInfo : prodInfa) {
                if (aidProdInfo.getProd().getProdukt_id() == prodId) {
                    p = aidProdInfo.getProd();
                }
            }

            if (!p.isPujceno()) {

                if (!produktyPridane.isEmpty()) {
                    for (int i = 0; i < produktyPridane.size(); i++) {
                        if (produktyPridane.contains(p)) {
                            JOptionPane.showMessageDialog(this, "Produkt již přidán!", "Chyba", JOptionPane.ERROR_MESSAGE);
                            break;
                        } else {
                            produktyPridane.add(p);
                            lstModelProdukty.fireModelChanged();
                            break;
                        }
                    }
                } else {
                    produktyPridane.add(p);
                    lstModelProdukty.fireModelChanged();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Produkt již vypůjčen!", "Chyba", JOptionPane.ERROR_MESSAGE);
            }
        }

        

    }//GEN-LAST:event_btPridProdDoListActionPerformed

    private void btVytVypActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btVytVypActionPerformed
        zakaznikService.vytvVyp(jxdOd.getDate(), jxdDo.getDate(), celkovaCena, tfZakId.getText().trim(), produktyPridane);
        tblModelZakVyp.fireTableDataChanged();
        JOptionPane.showMessageDialog(this, "Výpůjčka vytvořena.");
        vypInfa = zakaznikService.getVypInfa();
        tblModelZakVyp.update(vypInfa);
        prodInfa = zakaznikService.getProdukty();
        tblModelProdukty.update(prodInfa);

    }//GEN-LAST:event_btVytVypActionPerformed

