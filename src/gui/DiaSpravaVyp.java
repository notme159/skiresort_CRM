    private void btSpocAZvalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSpocAZvalActionPerformed
        int pocetDni = spoctiDobuPujc();

        for (Produkt p : produktyPridane) {
            p.getCena();
            celkovaCena += pocetDni * p.getCena();
        }

        tfCelkCen.setText(celkovaCena + "");

    }//GEN-LAST:event_btSpocAZvalActionPerformed

    private void btVracenoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btVracenoActionPerformed

        int row = jTaZakVyp.getSelectedRow();

        if (row != -1) {
            // 3. sloupec, pocitani od nuly, je vypID
            vypId = String.valueOf(jTaZakVyp.getModel().getValueAt(row, 3));

            if (cbVracenoVcas.isSelected()) {
                zakaznikService.setVracenoVcas(true);
            } else {
                zakaznikService.setVracenoVcas(false);
            }

            zakaznikService.odstrVyp(Integer.parseInt(vypId.trim()));
        }

        prodInfa = zakaznikService.getProdukty();
        tblModelProdukty.update(prodInfa);

        vypInfa = zakaznikService.getVypInfa();
        tblModelZakVyp.update(vypInfa);
    }//GEN-LAST:event_btVracenoActionPerformed

